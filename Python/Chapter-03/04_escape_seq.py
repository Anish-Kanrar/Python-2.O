#Escape sequence characters
# In Python, escape sequence characters are used to represent certain special characters within a string. They are preceded by a backslash ("\"). Here are some commonly used escape sequences in Python:
'''
\\ : Backslash (\)
\' : Single quote (')
\" : Double quote (")
\n : Newline
\t : Horizontal tab
\r : Carriage return
\b : Backspace
\f : Form feed
\v : Vertical tab
\a : Bell/alert
'''

a = "Anik is a good boy\nbut a bad\tboy"
b = "Animesh is a good boy but a bad \"boy\""

print(a)
print(b)
